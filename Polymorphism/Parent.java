package Polymorphism;

public class Parent {
	public void Money() {
		System.out.println("Money-->1 Lakh");
	}
	public void Car() {
		System.out.println("Car-->Swift");
	}
	public void Home() {
		System.out.println("Home-->2 BHK");
	}
	public void m1() {
		System.out.println("Parent Non-Static Method:M1");
	}

}
