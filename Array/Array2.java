package Array;

public class Array2 {

	public static void main(String[] args) {
		//Double Array
		double d[]=new double[4]; //Array Declaration
		
		d[0]=10d;
		d[1]=20;
		d[2]=30;
		d[3]=40;
		
		System.out.println(d.length);
		
		for(int i=0;i<d.length;i++) {
			System.out.println(d[i]);
		}
		

	}

}
