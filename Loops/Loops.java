package Loops;

public class Loops {

	public static void main(String[] args) {
		//Print 1 to 5 numbers using For_Loop
		for(int i=1;i<=5;i++) {
			System.out.println(i);
		}
		System.out.println();
		//Print 1 to 5 numbers using While_Loop
		int j=1;
		while(j<=5) {
			System.out.println(j);
			j++;
		}
		System.out.println();
		//Print 1 to 5 numbers using Do_While Loop
		int k=1;
		do {
			System.out.println(k);
			k++;
		}
		while(k<=5);

	}

}
