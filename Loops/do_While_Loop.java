package Loops;

public class do_While_Loop {

	public static void main(String[] args) {
//Syntax-->do{
//            Printing statement
//             Increment/Decrement
//	          }
//		   while(End condition)
		
		//Print 1 to 3 numbers using do_while loop
		int i=1; //Start condition
		do {
			System.out.println(i);
			i++;
		}
		while(i<=3); //End condition
		

	}

}
