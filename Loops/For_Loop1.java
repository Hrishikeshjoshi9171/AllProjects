package Loops;

public class For_Loop1 {

	public static void main(String[] args) {
		//Printing 5 to 1 and 1 to 5 numbers using for_loop
		for(int i=5;i>=1;i--) {
			System.out.print(" "+i);
		}
		System.out.println();
		System.out.println("*********************");
		
		for(int i=1;i<=5;i++) {
			System.out.print(" "+i);
		}

	}

}
